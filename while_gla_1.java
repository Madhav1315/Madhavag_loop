import java.util.Scanner;
public class while_gla_1 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("enter value of n");
        int n=sc.nextInt();
        int i=0;
        while(i<n)
        {
            i++;
            System.out.println(i);

        }
    }
}
