import java.util.Scanner;
public class while_gla_2 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("enter value of n");
        int n=sc.nextInt();
        int i=n;
        while(i>0)
        {
            System.out.println(i);
            i--;

        }
    }
}
